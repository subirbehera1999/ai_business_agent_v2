# Internal helpers
# ==============================================================================

async def _load_business(
    ctx: JobContext,
    log_extra: dict,
) -> Optional[BusinessModel]:
    """
    Load and validate the business profile.

    Skips businesses that are not found, inactive, deleted, or missing
    a WhatsApp number. Businesses without Google integration are still
    eligible — content does not require Google reviews to run (context
    gathering will simply return defaults).

    Args:
        ctx:       JobContext with db and business_repo.
        log_extra: Structured log context dict.

    Returns:
        BusinessModel if valid and active, else None.
    """
    business = await ctx.business_repo.get_by_id(ctx.db, ctx.business_id)

    if not business:
        logger.warning(
            "Content job skipped — business not found",
            extra=log_extra,
        )
        return None

    if not business.is_active or business.is_deleted:
        logger.info(
            "Content job skipped — business inactive or deleted",
            extra={**log_extra, "business_name": business.business_name},
        )
        return None

    if not business.owner_whatsapp_number:
        logger.info(
            "Content job skipped — no WhatsApp number configured",
            extra={**log_extra, "business_name": business.business_name},
        )
        return None

    return business


async def _gather_review_context(
    ctx: JobContext,
    log_extra: dict,
) -> tuple[float, str]:
    """
    Gather avg_rating and dominant_sentiment from the last 14 days of reviews.

    Both values are used as context for the content generation prompt.
    Defaults to (4.0, "positive") if no reviews exist or queries fail —
    a neutral positive signal that produces safe, encouraging content.

    Args:
        ctx:       JobContext with db and review_repo.
        log_extra: Structured log context dict.

    Returns:
        tuple[float, str]: (avg_rating, dominant_sentiment).
        avg_rating is 1.0–5.0. dominant_sentiment is "positive",
        "negative", or "neutral".
    """
    since_dt = datetime.now(timezone.utc) - timedelta(days=_CONTEXT_WINDOW_DAYS)

    # Default fallback values
    avg_rating: float = 4.0
    dominant_sentiment: str = ReviewSentiment.POSITIVE

    # Average rating
    try:
        fetched_avg = await ctx.review_repo.get_average_rating_since(
            ctx.db,
            business_id=ctx.business_id,
            since=since_dt,
        )
        if fetched_avg is not None:
            avg_rating = float(fetched_avg)
    except Exception as exc:
        logger.debug(
            "Failed to fetch avg rating for content context — using default",
            extra={**log_extra, "error": str(exc)},
        )

    # Dominant sentiment — pick the most common sentiment label
    try:
        sentiment_counts = await ctx.review_repo.count_by_sentiment_since(
            ctx.db,
            business_id=ctx.business_id,
            since=since_dt,
        )
        dominant_sentiment = _dominant_sentiment_from_counts(sentiment_counts)
    except Exception as exc:
        logger.debug(
            "Failed to fetch sentiment counts for content context — using default",
            extra={**log_extra, "error": str(exc)},
        )

    return avg_rating, dominant_sentiment


def _dominant_sentiment_from_counts(counts: dict[str, int]) -> str:
    """
    Determine the dominant sentiment label from a sentiment count dict.

    The dict has keys "positive", "negative", "neutral" with int counts.
    Returns the label with the highest count. Ties are broken by priority:
    positive > neutral > negative (to err on the side of optimistic content).

    Args:
        counts: Dict from review_repo.count_by_sentiment_since().

    Returns:
        str: "positive", "negative", or "neutral".
    """
    if not counts or all(v == 0 for v in counts.values()):
        return ReviewSentiment.POSITIVE

    # Priority order for tie-breaking
    priority = [
        ReviewSentiment.POSITIVE,
        ReviewSentiment.NEUTRAL,
        ReviewSentiment.NEGATIVE,
    ]

    # Find max count, then apply priority for tie-breaking
    max_count = max(counts.values())
    for label in priority:
        if counts.get(label, 0) == max_count:
            return label

    return ReviewSentiment.POSITIVE


def _extract_special_context(business: BusinessModel) -> Optional[str]:
    """
    Extract a special context string from the business description field.

    The business_description column stores optional context the owner
    registered during onboarding — e.g. "We now offer home delivery" or
    "New branch opening in December". This is injected into the content
    generation prompt to make posts timely and specific.

    Truncated to _MAX_SPECIAL_CONTEXT_CHARS to keep the prompt size
    within the token budget.

    Args:
        business: BusinessModel instance.

    Returns:
        str if business_description is non-empty, else None.
    """
    if not business.business_description:
        return None

    description = business.business_description.strip()
    if not description:
        return None

    if len(description) > _MAX_SPECIAL_CONTEXT_CHARS:
        return description[:_MAX_SPECIAL_CONTEXT_CHARS]

    return description